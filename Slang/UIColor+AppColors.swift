import UIKit

extension UIColor {
    
    class func PrimaryBrandColor() -> UIColor {
        return UIColor(red:0.212, green:0.212, blue:0.306, alpha: 1)
    }
    
    class func SecondaryBrandColor() -> UIColor {
        return UIColor(red:0.992, green:0.431, blue:0.431, alpha: 1)
    }
}
